#include "../../includes/libft.h"

size_t	ft_nbrlen_i(int n)
{
	int	nlen;

	nlen = 0;
	if (n <= 0)
		nlen++;
	while (n != '\0')
	{
		nlen++;
		n = n / 10;
	}
	return (nlen);
}
